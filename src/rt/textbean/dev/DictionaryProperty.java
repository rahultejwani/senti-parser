package rt.textbean.dev;

import java.util.Properties;

public class DictionaryProperty extends Properties{

	/**
	 * Properties extension to read dictionary files as a property file with Key Value Pairs.
	 * Implements getKey Method to return the keys from the file.
	 */
	private static final long serialVersionUID = 1L;
	
	public String getKey()
	{
		return this.getKey();
	}

}
